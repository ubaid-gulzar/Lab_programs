// program to swap two numbers
#include<stdio.h>
int main(){
        int a,b,c;
        printf("enter the value of a,b");
        scanf("%d %d %d",&a,&b,&c);
        c=a;
        c=b;
        printf("number before swapping are %d and %d",a,b);
        printf("number after swapping are %d and %d",a,b);
        return 0;
} 